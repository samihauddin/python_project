
# Calculator Functions

def add(int1, int2):
    return int1 + int2

def subtract(int1, int2):
    return int1 - int2

def multiply(int1, int2):
    return int1 * int2

def divide (int1, int2):
    return int2 / int1

# main calculator programme

print(add(3, 5))
print(subtract(6,2))
print(multiply(7,4))
print(divide(2,8))

